# ═══════════════════════════════════════════════════════════
#  Hook System (Extended with PreLLMCall & PostLLMCall)
# ═══════════════════════════════════════════════════════════

HOOKS = {
    "UserPromptSubmit": [], 
    "PreToolUse": [], 
    "PostToolUse": [], 
    "Stop": [],
    "PreLLMCall": [],   # 新增：发给 LLM 的请求 Hook
    "PostLLMCall": []   # 新增：LLM 返回的响应 Hook
}

def register_hook(event: str, callback):
    HOOKS[event].append(callback)

def trigger_hooks(event: str, *args):
    for callback in HOOKS[event]:
        result = callback(*args)
        if result is not None:
            return result
    return None

# s04 hooks preserved
DENY_LIST = ["rm -rf /", "sudo", "shutdown", "reboot", "mkfs", "dd if="]

def permission_hook(block):
    """PreToolUse: deny list check."""
    if block.name == "bash":
        for p in DENY_LIST:
            if p in block.input.get("command", ""):
                print(f"\n\033[31m⛔ Blocked: '{p}'\033[0m")
                return "Permission denied"
    return None

def log_hook(block):
    """PreToolUse: log tool calls."""
    print(f"\033[90m[HOOK] {block.name}\033[0m")
    return None

def context_inject_hook(query: str):
    """UserPromptSubmit: log working directory."""
    print(f"\033[90m[HOOK] UserPromptSubmit: working in {WORKDIR}\033[0m")
    return None

def summary_hook(messages: list):
    """Stop: print tool call count."""
    tool_count = sum(1 for m in messages
                     for b in (m.get("content") if isinstance(m.get("content"), list) else [])
                     if isinstance(b, dict) and b.get("type") == "tool_result")
    print(f"\033[90m[HOOK] Stop: session used {tool_count} tool calls\033[0m")
    return None

# ── 新增需求实现：Full Request & Response JSON Logger ───────────────

def _json_default(obj):
    """通用对象转 JSON 的辅助函数，兼容 SDK 对象"""
    if hasattr(obj, "model_dump"):
        return obj.model_dump()
    if hasattr(obj, "__dict__"):
        return obj.__dict__
    return str(obj)

def log_pre_llm_hook(request_payload: dict):
    """PreLLMCall: 将发给 LLM 的完整 Request Payload 以格式化 JSON 追加保存到 agent.log"""
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    formatted_json = json.dumps(request_payload, indent=2, ensure_ascii=False, default=_json_default)
    
    with open(LOG_FILE, "a", encoding="utf-8") as f:
        f.write(f"\n==================== [PRE LLM CALL] {timestamp} ====================\n")
        f.write(formatted_json + "\n")
    return None

def log_post_llm_hook(response):
    """PostLLMCall: 将 LLM 返回的完整 Response 以格式化 JSON 追加保存到 agent.log"""
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    resp_dict = response.model_dump() if hasattr(response, "model_dump") else response
    formatted_json = json.dumps(resp_dict, indent=2, ensure_ascii=False, default=_json_default)
    
    with open(LOG_FILE, "a", encoding="utf-8") as f:
        f.write(f"\n==================== [POST LLM CALL] {timestamp} ====================\n")
        f.write(formatted_json + "\n")
    return None

# 注册所有 Hook
register_hook("UserPromptSubmit", context_inject_hook)
register_hook("PreToolUse", permission_hook)
register_hook("PreToolUse", log_hook)
register_hook("Stop", summary_hook)
register_hook("PreLLMCall", log_pre_llm_hook)
register_hook("PostLLMCall", log_post_llm_hook)


# ═══════════════════════════════════════════════════════════
#  agent_loop — with Pre/Post LLM Hooks integrated
# ═══════════════════════════════════════════════════════════

rounds_since_todo = 0

def agent_loop(messages: list):
    global rounds_since_todo
    while True:
        # nag reminder — inject if model hasn't updated todos for 3 rounds
        if rounds_since_todo >= 3 and messages:
            messages.append({"role": "user",
                             "content": "<reminder>Update your todos.</reminder>"})
            rounds_since_todo = 0

        # 构建完整的 API 请求 payload 参数
        request_payload = {
            "model": MODEL,
            "system": SYSTEM,
            "messages": messages,
            "tools": TOOLS,
            "max_tokens": 8000
        }

        # 触发 PreLLMCall Hook（保存发给 LLM 的完整 JSON）
        trigger_hooks("PreLLMCall", request_payload)

        # 发起真实 API 调用
        response = client.messages.create(**request_payload)

        # 触发 PostLLMCall Hook（保存 LLM 返回的完整 JSON）
        trigger_hooks("PostLLMCall", response)

        messages.append({"role": "assistant", "content": response.content})

        if response.stop_reason != "tool_use":
            force = trigger_hooks("Stop", messages)
            if force:
                messages.append({"role": "user", "content": force})
                continue
            return

        rounds_since_todo += 1
        results = []
        for block in response.content:
            if block.type != "tool_use":
                continue

            blocked = trigger_hooks("PreToolUse", block)
            if blocked:
                results.append({"type": "tool_result", "tool_use_id": block.id,
                                "content": str(blocked)})
                continue

            handler = TOOL_HANDLERS.get(block.name)
            output = handler(**block.input) if handler else f"Unknown: {block.name}"

            trigger_hooks("PostToolUse", block, output)

            # reset nag counter when todo_write is called
            if block.name == "todo_write":
                rounds_since_todo = 0

            results.append({"type": "tool_result", "tool_use_id": block.id,
                            "content": output})

        messages.append({"role": "user", "content": results})


